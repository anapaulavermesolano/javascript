palabra= prompt("Escribe casa o mesa o perro o gato para traducirlo al ingles");
switch (palabra) {
	case 'casa':
		alert("casa en ingles es: house");
		break;
	case 'mesa':
		alert("mesa en ingles es: table");
		break;
	case 'perro':
		alert("perro en ingles es: dog");
		break;
	case 'gato':
		alert("gato en ingles es: cat");
		break;
}